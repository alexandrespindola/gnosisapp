// Function to get a translation by key
export function useTranslations(locale: string) {
  // Fallback to Spanish if the locale is not supported
  const currentTranslations = translations[locale] || translations.es;
  
  return function t(key: string): string {
    // Supports nested keys like 'books.viewMore'
    return key.split('.').reduce((obj, k) => obj && obj[k], currentTranslations) || key;
  };
}
